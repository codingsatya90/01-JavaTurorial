package string.in.sit;

public class ConcatMethodDemo01 {

	public static void main(String[] args) {
		
		String city1="Patna";
		String city2="Gaya";
		System.out.println(city1.concat(city2));
		
		
		//toUpperCase() Method
		System.out.println(city1.toUpperCase());
		
		//toLowerCase() Method
		System.out.println(city2.toLowerCase());
	}
}
