package arrays.in.sit;

public class MultiDimensionArray03 {

	public static void main(String[] args) {

		int arr[][] = new int[2][2];
		arr[0][0] = 100;
		arr[0][1] = 200;
		arr[1][0] = 300;
		arr[1][1] = 400;
		
		
		
		for(int ar[]:arr) {
			for(int n:ar) {
				System.out.println(n);
			}
		}
	}
}
