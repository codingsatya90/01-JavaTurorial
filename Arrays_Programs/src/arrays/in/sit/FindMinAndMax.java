public class FindMinAndMax {

	
	//program with sort method
	public static void main(String[] args) {
		int arr[]= {12,4,6,23,2,7,9};
		
		Arrays.sort(arr);
		
		System.out.println("Min Element:"+arr[0]);
		System.out.println("Max Element:"+arr[arr.length-1]);
	}
}
